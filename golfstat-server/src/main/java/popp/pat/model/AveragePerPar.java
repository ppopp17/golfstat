package popp.pat.model;

public class AveragePerPar {
  private Long par;
  private Float score;
  private Float putts;

  public Long getPar() {
    return par;
  }

  public void setPar(Long par) {
    this.par = par;
  }

  public Float getTotal() {
    return score;
  }

  public void setTotal(Float score) {
    this.score = score;
  }

  public Float getPutts() {
    return putts;
  }

  public void setPutts(Float putts) {
    this.putts = putts;
  }
}
