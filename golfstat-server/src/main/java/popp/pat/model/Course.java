package popp.pat.model;

public class Course {
	Long id;
	String name;
	String tees;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTees() {
		return tees;
	}
	public void setTees(String tees) {
		this.tees = tees;
	}
}
