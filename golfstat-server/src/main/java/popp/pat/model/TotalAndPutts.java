package popp.pat.model;

public class TotalAndPutts {
  private Long total;
  private Long putts;



	/**
	* Returns value of total
	* @return
	*/
	public Long getTotal() {
		return total;
	}

	/**
	* Sets new value of total
	* @param
	*/
	public void setTotal(Long total) {
		this.total = total;
	}

	/**
	* Returns value of putts
	* @return
	*/
	public Long getPutts() {
		return putts;
	}

	/**
	* Sets new value of putts
	* @param
	*/
	public void setPutts(Long putts) {
		this.putts = putts;
	}
}
